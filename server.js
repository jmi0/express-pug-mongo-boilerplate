const express = require('express');

/*******************************************************************************
*******************************************************************************/
// Set a port
let PORT = process.env.PORT || 8181;


/*******************************************************************************
*******************************************************************************/
// Create app instance
const app = express();


/*******************************************************************************
*******************************************************************************/
// set view engine
app.set("view engine", "pug");

/*******************************************************************************
*******************************************************************************/
// index endpoint
app.get('/', (req, res) => {
	res.render('index', { message: "Hello from express and pug!" });
});


/*******************************************************************************
*******************************************************************************/
// listen
app.listen(PORT, () => {
	console.log(`starting server on port ${PORT}`);
});
